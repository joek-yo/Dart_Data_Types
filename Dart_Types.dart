void main() {
    // Integer (int) - Stores whole numbers
    int age = 30;
    print("My age is: $age");

    // Double (double) - stores decimal numbers
    double pi = 3.14159;
    print("The value of pi is: $pi");

    // String (String) - Stores text data
    String name = "Joseph Kihiu";
    print("Hello, my name is: $name");

    // List (List) - Stores an ordered collection of items
    List<String> colors = ["red", "green", "blue"];
    print("Primary colors: $colors");

    // Map (Map) - Stores key-value pairs
    Map<String, String> fruits = {"apple": "red", "banana": "yellow"};
    print("Fruit color: ${fruits["apple"]}");
}